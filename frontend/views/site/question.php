一、如何组织一次拍摄？
 

1、发布拍摄计划。

2、通过“找拍档”邀请合适的人员加入计划。

3、通过在“拍友秘籍”的“人员招募”板块发帖吸引人员主动申请加入你的计划。

4、与可能的人员进行较详细沟通以确保团队的顺利组建。

5、从我的主页进入“我发布的计划”来进行计划管理与人员管理。

6、线下进行作品的拍摄的同时利用我们的网站进行团队交流和吸引粉丝。

7、上传作品。

 

