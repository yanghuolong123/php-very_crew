三、如何获得更多关注与合作机会？

 

1、完善个人头像、联系方式、特长等资料。

2、拍出好的作品并积累经验值与等级就是最好的展示。

3、在留言板和“拍友秘籍”宣传自己的作品，积累粉丝。还可以分享到其它相关平台获得关注。

